public class Articulo {
	public int codigo;
	public String nombre;
	public int precio;
	
	public Articulo() {
		
		super();
		
	}
	
	
	public Articulo(int codigo, String nombre ,int precio) {
		super();
	
		this.codigo = codigo;
		this.precio = precio;
		this.nombre = nombre;
		
		
	}
	
}
